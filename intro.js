/*
    Console.log() : 
    To write our first JavaScript code, we used a built-in function console.log(). We passed an argument as input data.
    and the function displays the output. We passed 'Hello,World' as input data or argument in the console.log() function.
*/
console.log('Hello,World!');

/*
    Console.log with Multiple Arguments
    The console.log() function can take multiple parameters separated by commas. The syntax looks like as follows : 
    console.log(param1,param2,param3);
*/
console.log('Hello','World','!');
console.log('HAPPY','NEW','YEAR',2020);
console.log('Welcome','to','30','Days','Of','JavaScript');

/*
    As you can see from the snippet code above, console.log() can take multiple arguments.
    Cong. You wrote your first JavaScript code using console.log().
*/
